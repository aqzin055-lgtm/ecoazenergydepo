let currentLang = 'az';

function applyLanguage(lang) {
    const t = translations[lang];
    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if (t[key] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.innerText = t[key];
            }
        }
    });
    
    const langBtn = document.getElementById('langBtn');
    if (langBtn) {
        if (lang === 'az') langBtn.innerHTML = 'AZ 🌐';
        else if (lang === 'ru') langBtn.innerHTML = 'RU 🌐';
        else langBtn.innerHTML = 'EN 🌐';
    }
    
    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    // Reveal Animations
    const reveals = document.querySelectorAll('.reveal');
    const observer = new IntersectionObserver((entries, obs) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                obs.unobserve(entry.target);
            }
        });
    }, { threshold: 0.15, rootMargin: '0px 0px -50px 0px' });

    reveals.forEach(el => observer.observe(el));

    // Language Selector
    const langSelector = document.getElementById('langSelector');
    const langBtn = document.getElementById('langBtn');
    
    if (langBtn && langSelector) {
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langSelector.classList.toggle('open');
        });
    }

    document.addEventListener('click', () => {
        if (langSelector && langSelector.classList.contains('open')) {
            langSelector.classList.remove('open');
        }
    });

    const langOptions = document.querySelectorAll('.lang-dropdown button');
    langOptions.forEach(btn => {
        btn.addEventListener('click', (e) => {
            const selectedLang = e.target.getAttribute('data-lang');
            currentLang = selectedLang;
            applyLanguage(selectedLang);
            langSelector.classList.remove('open');
        });
    });

    // Smooth Scroll for Anchors
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });

    // Apply Default Language
    applyLanguage(currentLang);
});
